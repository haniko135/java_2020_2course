package ru.ikbo1319.experement.ex_7.pr_2;

public class York extends Dog{
    public York(String dog, double age, String name, String size, double height, String sherst) {
        super(dog, age, name, size, height, sherst);
    }

    @Override
    public String getDog() {
        return super.getDog();
    }

    @Override
    public void setDog(String dog) {
        super.setDog(dog);
    }

    @Override
    public double getAge() {
        return super.getAge();
    }

    @Override
    public void setAge(double age) {
        super.setAge(age);
    }

    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public void setName(String name) {
        super.setName(name);
    }

    @Override
    public String getSize() {
        return super.getSize();
    }

    @Override
    public void setSize(String size) {
        super.setSize(size);
    }

    @Override
    public double getHeight() {
        return super.getHeight();
    }

    @Override
    public void setHeight(double height) {
        super.setHeight(height);
    }

    @Override
    public String getSherst() {
        return super.getSherst();
    }

    @Override
    public void setSherst(String sherst) {
        super.setSherst(sherst);
    }

    @Override
    public String toString() {
        return super.toString();
    }

    @Override
    public String voice() {
        return "vaf-vaf-vaf";
    }
}
